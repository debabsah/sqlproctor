def build(db_path: str = DB_PATH, *, sf: float = DEFAULT_SF) -> str:
    """Generate a real TPC-DS warehouse at scale `sf` via DuckDB's tpcds extension."""
    if os.path.exists(db_path):
        os.remove(db_path)
    con = duckdb.connect(db_path)
    con.execute("INSTALL tpcds")
    con.execute("LOAD tpcds")
    con.execute(f"CALL dsdgen(sf={sf})")
    con.close()
    return db_path


# The canonical correct answer to "total store sales" (single fact, no fan-out) verifies.
TRUE_REVENUE_SQL = """
SELECT SUM(ss.ss_net_paid) AS store_revenue
FROM store_sales ss
"""

# "Net sales after returns, by item" written the tempting way: join the item dimension
# to BOTH facts. Each item's sales rows get multiplied by its return rows (~20x at sf=1).
NAIVE_REVENUE_SQL = """
SELECT SUM(ss.ss_net_paid) AS store_revenue
FROM item i
JOIN store_sales ss   ON i.i_item_sk = ss.ss_item_sk
JOIN store_returns sr ON i.i_item_sk = sr.sr_item_sk
"""


def revenue_numbers(db_path: str = DB_PATH):
    """Returns (true_revenue, naive_revenue, ratio) from the real generated DB."""
    con = duckdb.connect(db_path, read_only=True)
    true_rev = float(con.execute(TRUE_REVENUE_SQL).fetchone()[0])
    naive_rev = float(con.execute(NAIVE_REVENUE_SQL).fetchone()[0])
    con.close()
    return true_rev, naive_rev, naive_rev / true_rev


def _counts(db_path: str):
    con = duckdb.connect(db_path, read_only=True)
    q = lambda sql: con.execute(sql).fetchone()[0]
    out = {t: q(f"SELECT COUNT(*) FROM {t}")
           for t in ("store_sales", "store_returns", "item", "customer", "store")}
    con.close()
    return out


def main() -> int:
    path = build()
    c = _counts(path)
    true_rev, naive_rev, ratio = revenue_numbers(path)
    print(f"Seeded {path}  (TPC-DS sf={DEFAULT_SF}, store channel)")
    print(f"  store_sales:     {c['store_sales']:>12,}")
    print(f"  store_returns:   {c['store_returns']:>12,}")
    print(f"  item / customer / store: {c['item']:,} / {c['customer']:,} / {c['store']:,}")
    print(f"  true store sales:  {true_rev:>18,.2f}   SUM(ss_net_paid)")
    print(f"  naive (item->sales+returns): {naive_rev:>18,.2f}")
    print(f"  inflation:         {ratio:>10.2f}x")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
