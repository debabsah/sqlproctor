import pathlib

import duckdb

HERE = pathlib.Path(__file__).resolve().parent
DB_PATH = os.environ.get("SQLPROCTOR_DB", str(HERE / "tpcds.duckdb"))
DEFAULT_SF = float(os.environ.get("SQLPROCTOR_TPCDS_SF", "1"))

