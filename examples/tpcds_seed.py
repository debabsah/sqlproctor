"""Seed a real TPC-DS store-channel warehouse where a naive dimension join inflates
sales. Unlike the retail/saas demos, the data is NOT hand-seeded: it is generated by
DuckDB's official `tpcds` extension (`CALL dsdgen(sf=...)`), so the schema and the
numbers are the industry-standard benchmark, reproducible by anyone.

The trap: store_sales and store_returns are both children of the item (and customer,
date, store) dimension. Joining a dimension to BOTH facts and summing a sales measure
cross-products each item's sales by its returns, so revenue balloons ~20x at sf=1.
That is a silent, multiplicative error on a schema no one hand-built to be trappy.

Scale factor: SQLPROCTOR_TPCDS_SF (default 1, the canonical minimum). dsdgen builds all
24 TPC-DS tables; the contract and the model prompt scope to the six store-channel
tables below.

Run: python examples/tpcds_seed.py
"""

